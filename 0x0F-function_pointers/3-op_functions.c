#include "3-calc.h"

/**
 * op_add - adds 2 integers
 * @a: integer 1
 * @b: integer 2
 *
 * Return: the sum of a and b
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtraction of 2 integers
 * @a: integer 1
 * @b: integer 2
 *
 * Return: the difference of a and b
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies 2 integers
 * @a: integer 1
 * @b: integer 2
 *
 * Return: the produc of a and b
*/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides 2 integers
 * @a: integer 1
 * @b: integer 2
 *
 * Return: a / b
*/
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulus of 2 integers
 * @a: integer 1
 * @b: integer 2
 *
 * Return: a % b
*/
int op_mod(int a, int b)
{
	return (a % b);
}
